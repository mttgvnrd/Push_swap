#include "libft.h"

int	ft_countdec(int n)
{	
	int			i;
	long int	a;

	a = n;
	if (a == 0)
		return (1);
	i = 0;
	if (a < 0)
	{
		a *= -1;
		i = 1;
	}
	while (a > 0)
	{
		i++;
		a = a / 10;
	}
	return (i);
}
